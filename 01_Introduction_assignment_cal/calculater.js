//method for add: 
const add = function (num1, num2) {
    return num1 + num2;
}

//method for subtract:
const subtract = function (num1, num2) {
    return num1 - num2;
}

//method for multiply:
const multiply = function (num1, num2) {
    return num1 * num2;
}

//method for divide:
const divide = function (num1, num2) {
    return num1 / num2;
}

exports.add = add; 
exports.subtract = subtract; 
exports.multiply = multiply;
exports.divide = divide;
